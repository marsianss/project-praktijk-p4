      <div class="contact-form">
        <details>
          <summary> Welke onderwerpen worden behandeld in de Robotic-Ethiek cursus?</summary>
           <div class="faq-content">De Robotic-Ethiek cursus behandelt verschillende onderwerpen die de kruising van robotica, kunstmatige intelligentie en ethische overwegingen verkennen. Belangrijke onderwerpen zijn onder meer:

            De geschiedenis en evolutie van robotica en AI.
            Ethische theorieën en kaders die van toepassing zijn op technologie.
            De maatschappelijke impact van robotica en AI, inclusief kwesties van privacy, veiligheid en baanverlies.
            Casestudy's over de ethische implicaties van specifieke toepassingen van robotica, zoals autonome voertuigen, drones en medische robots.
            Juridische en regelgevende aspecten van robotica en AI.
            Het ontwerpen en programmeren van ethische AI-systemen.</div>
        </details>
        <details>
          <summary>Wie kan zich inschrijven voor de Robotic-Ethiek cursus?</summary>
           <div class="faq-content">De Robotic-Ethiek cursus staat open voor alle studenten, ongeacht hun studierichting. Het is bijzonder nuttig voor studenten die een diploma in informatica, techniek, filosofie, rechten en sociale wetenschappen nastreven. Er zijn geen toelatingseisen, waardoor het toegankelijk is voor iedereen die geïnteresseerd is in het begrijpen van de ethische dimensies van robotica en AI.</div>
        </details>
        <details>
          <summary> Hoe zal deze cursus mijn toekomstige carrière ten goede komen?</summary>
          <div class="faq-content">Deze cursus biedt waardevolle inzichten en vaardigheden die steeds belangrijker worden in de moderne arbeidsmarkt. Door de ethische implicaties van robotica en AI te begrijpen, ben je beter uitgerust om:

            De complexe morele en ethische uitdagingen in technologiegerelateerde carrières te navigeren.
            Bij te dragen aan de ontwikkeling van verantwoorde en ethische technologische oplossingen.
            Je kritisch denkvermogen en ethische redeneervaardigheden te verbeteren, die hoog gewaardeerd worden in veel beroepen.
            Op de hoogte te blijven van huidige en toekomstige trends in technologie en hun maatschappelijke impact.
            Een concurrentievoordeel te behalen in gebieden zoals AI-ontwikkeling, robotica-engineering, juridisch advies en openbaar beleid.</div>
        </details>
        <details>
          <summary>Wat is het formaat van de cursus en hoe worden studenten beoordeeld?</summary>
          <div class="faq-content">De Robotic-Ethiek cursus is gestructureerd als een combinatie van lezingen, discussies, casestudy-analyses en praktische projecten. Studenten worden beoordeeld op verschillende manieren, waaronder:

            Schriftelijke opdrachten en essays die ethische dilemma's analyseren.
            Deelname aan klasdiscussies en debatten.
            Groepsprojecten die het creëren van ethische kaders voor hypothetische of reële toepassingen van robotica omvatten.
            Quizzen en examens die de kennis van cursusmateriaal testen.
            Een eindproject of paper dat studenten in staat stelt om diep in te gaan op een specifiek ethisch vraagstuk met betrekking tot robotica en AI.</div>
        </details>
        <details>
          <summary>Welke voorkennis is vereist voor de Robotic-Ethiek cursus?</summary>
          <div class="faq-content">Er is geen specifieke voorkennis vereist voor de Robotic-Ethiek cursus. Echter, basiskennis van robotica, kunstmatige intelligentie, of ethiek kan nuttig zijn. De cursus is ontworpen om toegankelijk te zijn voor zowel beginners als gevorderden.</div>
        </details>
        <details>
          <summary>Hoe lang duurt de Robotic-Ethiek cursus?</summary>
          <div class="faq-content">De duur van de Robotic-Ethiek cursus varieert, afhankelijk van het programma en de instelling die de cursus aanbiedt. Meestal duurt de cursus een semester, wat ongeveer 12 tot 14 weken is, met wekelijkse sessies.</div>
        </details>
        <details>
          <summary>Zijn er online bronnen of literatuur die ik kan raadplegen voor de cursus?</summary>
          <div class="faq-content">Ja, er zijn verschillende online bronnen en literatuur die je kunt raadplegen voor de cursus. Aanbevolen boeken zijn onder andere "Robot Ethics 2.0" en "Artificial Intelligence: A Guide for Thinking Humans". Online platforms zoals Coursera en edX bieden ook relevante cursussen en materialen aan.</div>
        </details>
        <details>
          <summary>Wat zijn de kosten van de Robotic-Ethiek cursus?</summary>
          <div class="faq-content">De kosten van de Robotic-Ethiek cursus kunnen variëren afhankelijk van de instelling die de cursus aanbiedt. Universitaire cursussen kunnen deel uitmaken van het reguliere collegegeld, terwijl online cursussen op platforms zoals Coursera of edX kunnen variëren van gratis tot enkele honderden euro's voor een volledige cursus.</div>
        </details>
        <details>
          <summary>Zijn er mogelijkheden voor praktische toepassingen tijdens de cursus?</summary>
          <div class="faq-content">Ja, de Robotic-Ethiek cursus omvat vaak praktische toepassingen zoals casestudy-analyses en groepsprojecten. Deze projecten stellen studenten in staat om ethische kaders te ontwikkelen en toe te passen op hypothetische of reële scenario's in de robotica en AI.</div>
        </details>
